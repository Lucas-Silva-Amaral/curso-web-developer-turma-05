// Expressoes logicas

let x = 612;

// AND (&&)
if (x >= 0 && x <= 10) {
  console.log("Numero está entre 0 e 10");
} else {
  console.log("Numero não está entre 0 e 10");
}

// OR (||)

if (x < 0 || x > 10) {
  console.log("Numero está fora do intervalo 0 - 10");
} else {
  console.log("Numero está dentro do intervalo 0 - 10");
}
